#!/bin/bash
# ══════════════════════════════════════════════════════════════
#  量子认知引擎 — 一键更新脚本
#  使用方式：./deploy.sh
#  前提：已首次部署完毕且容器正在运行
#
#  这个脚本做三件事：
#  1. 拉取 GitHub 最新代码
#  2. 重新构建有变化的容器镜像
#  3. 滚动重启容器（不停服）
# ══════════════════════════════════════════════════════════════

set -e  # 任何步骤失败立即停止

# 切换到脚本所在目录（防止从其他目录调用时路径出错）
cd "$(dirname "$0")"

echo ""
echo "╔══════════════════════════════════════╗"
echo "║   量子认知引擎 — 代码更新中          ║"
echo "╚══════════════════════════════════════╝"
echo ""

# 1. 拉取最新代码
echo "📦 [1/3] 拉取最新代码..."
git pull origin main
echo ""

# 2. 重建镜像并更新容器（只重建有变化的 service）
echo "🔨 [2/3] 重建镜像（如果代码有变化）..."
docker compose build --no-cache
echo ""

# 3. 重启容器
echo "🚀 [3/3] 重启容器..."
docker compose up -d
echo ""

# 显示运行状态
echo "✅ 更新完成！当前运行状态："
docker compose ps
echo ""
echo "📋 查看实时日志：docker compose logs -f"
